def generate_life_script(persona):
    print(f"\n{'='*60}")
    print(f"正在生成: {persona['id']} - {persona['姓名']}, {persona['年龄']}岁, {persona['城市']}")
    print(f"{'='*60}")

    prompt = build_prompt(persona)
    result, think = llm.get_complete([{"role": "user", "content": prompt}])

    # 尝试解析 JSON
    try:
        script = json.loads(result)
        print(f"✓ {persona['id']} 生成成功")
        return script
    except json.JSONDecodeError:
        # 尝试从 markdown 代码块中提取
        json_match = re.search(r'```(?:json)?\s*([\s\S]*?)```', result)
        if json_match:
            try:
                script = json.loads(json_match.group(1))
                print(f"✓ {persona['id']} 生成成功（提取代码块）")
                return script
            except json.JSONDecodeError:
                pass

        # 尝试直接匹配最外层 {}
        json_match = re.search(r'\{[\s\S]*\}', result)
        if json_match:
            try:
                script = json.loads(json_match.group())
                print(f"✓ {persona['id']} 生成成功（提取JSON）")
                return script
            except json.JSONDecodeError:
                print(f"✗ {persona['id']} JSON解析失败，保存原始结果")
                return {"_raw": result, "_parse_error": True}
        else:
            print(f"✗ {persona['id']} 未找到JSON，保存原始结果")
            return {"_raw": result, "_parse_error": True}


def main():
    output_file = "life_scripts.json"

    # 加载已生成的剧本（支持断点续传）
    existing = {}
    if os.path.exists(output_file):
        with open(output_file, "r", encoding="utf-8") as f:
            existing = json.load(f)
        print(f"已加载 {len(existing)} 个现有剧本")

    # P01 手写剧本作为种子
    if "P01" not in existing:
        existing["P01"] = P01_SCRIPT
        print("已加入手写的 P01 剧本")

    # 逐个生成
    for persona in personas:
        pid = persona["id"]
        if pid in existing and not existing[pid].get("_parse_error"):
            print(f"跳过 {pid}（已存在）")
            continue

        script = generate_life_script(persona)
        existing[pid] = script

        # 每生成一个就保存一次（防止中断丢失）
        with open(output_file, "w", encoding="utf-8") as f:
            json.dump(existing, f, ensure_ascii=False, indent=2)
        print(f"已保存 {pid} 到 {output_file}")

    print(f"\n全部完成！共 {len(existing)} 个剧本，保存在 {output_file}")


if __name__ == "__main__":
    main()
